gender = {
    'm': 'Дорогой',
    'f': 'Дорогая',
}

a = [
    ['Семён', 'Семёнов', 32.56, 'm'],
    ['Зоя', 'Иванова', 378, 'f'],
    ['Катерина', 'Петрова', 65, 'f'],
]

for name, mid_name, balance, g in a:
    text = f"""{gender[g]} {name} {mid_name}, 
баланс Вашего лицевого счёта составляет {balance} руб."""
    print(text)
